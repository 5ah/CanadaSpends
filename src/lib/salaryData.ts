// Utility function to adjust salary range for inflation
export const adjustSalaryRangeForInflation = (
  range: string,
  year: string,
): string => {
  const inflationFactor = inflationData[year as keyof typeof inflationData];
  if (!inflationFactor || inflationFactor === 1.0) return range;

  // Extract numbers from salary range
  const numbers = range.match(/\d+/g);
  if (!numbers) return range;

  const adjustedNumbers = numbers.map((num) => {
    const adjusted = Math.round(parseInt(num) * inflationFactor);
    return adjusted.toLocaleString();
  });

  // Reconstruct the range with adjusted numbers
  let adjustedRange = range;
  numbers.forEach((num, index) => {
    adjustedRange = adjustedRange.replace(num, adjustedNumbers[index]);
  });

  return adjustedRange;
};

